package com.wonjin.sherlockphones.vo;

public class UserVo {
	String u_email;
	String u_passwd;
	String u_name;
	String u_phonenum;
	String u_device_id;
	
	public String getU_email() {
		return u_email;
	}
	public void setU_email(String u_email) {
		this.u_email = u_email;
	}
	public String getU_passwd() {
		return u_passwd;
	}
	public void setU_passwd(String u_passwd) {
		this.u_passwd = u_passwd;
	}
	public String getU_name() {
		return u_name;
	}
	public void setU_name(String u_name) {
		this.u_name = u_name;
	}
	public String getU_phonenum() {
		return u_phonenum;
	}
	public void setU_phonenum(String u_phonenum) {
		this.u_phonenum = u_phonenum;
	}
	public String getU_device_id() {
		return u_device_id;
	}
	public void setU_device_id(String u_device_id) {
		this.u_device_id = u_device_id;
	}
	
}
