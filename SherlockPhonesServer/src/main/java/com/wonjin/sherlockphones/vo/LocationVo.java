package com.wonjin.sherlockphones.vo;

public class LocationVo {
	int l_id;
	String u_phonenum;
	double l_long;
	double l_lat;
	String l_time;
	public int getL_id() {
		return l_id;
	}
	public void setL_id(int l_id) {
		this.l_id = l_id;
	}
	public String getU_phonenum() {
		return u_phonenum;
	}
	public void setU_phonenum(String u_phonenum) {
		this.u_phonenum = u_phonenum;
	}
	public double getL_long() {
		return l_long;
	}
	public void setL_long(double l_long) {
		this.l_long = l_long;
	}
	public double getL_lat() {
		return l_lat;
	}
	public void setL_lat(double l_lat) {
		this.l_lat = l_lat;
	}
	public String getL_time() {
		return l_time;
	}
	public void setL_time(String l_time) {
		this.l_time = l_time;
	}
	
}
